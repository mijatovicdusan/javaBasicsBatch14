package com.syntax.class12;

public class StringDemoStartsWith {

	public static void main(String[] args) {

		
	String str2="Java is Very easy";
	
	System.out.println(str2.startsWith("J"));//startsWith--->checks if a String starts with a specific letter or word
	
	System.out.println(str2.endsWith("y"));//endsWith-->checks if a String ends with a specific letter or word
	
	System.out.println(str2.contains("very"));//-->checks if a String contains with a specific letter or word

	

		
	System.out.println(str2.toLowerCase().contains("very"));
	
	// method chaining helps us call multiple methods on a single line one method after another
		
		
		
		
		
		
		
	}

}
